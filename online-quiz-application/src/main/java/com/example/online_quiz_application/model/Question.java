package com.example.online_quiz_application.model;

public class Question {
    private int quizId;          // ID of the quiz this question belongs to
    private String questionText; // The question text
    private String optionA;      // Option A for the question
    private String optionB;      // Option B for the question
    private String optionC;      // Option C for the question
    private String optionD;      // Option D for the question
    private String correctAnswer; // The correct answer to the question

    // Default constructor
    public Question() {}

    // Getters and Setters

    public int getQuizId() {
        return quizId;
    }

    public void setQuizId(int quizId) {
        this.quizId = quizId;
    }

    public String getQuestionText() {
        return questionText;
    }

    public void setQuestionText(String questionText) {
        this.questionText = questionText;
    }

    public String getOptionA() {
        return optionA;
    }

    public void setOptionA(String optionA) {
        this.optionA = optionA;
    }

    public String getOptionB() {
        return optionB;
    }

    public void setOptionB(String optionB) {
        this.optionB = optionB;
    }

    public String getOptionC() {
        return optionC;
    }

    public void setOptionC(String optionC) {
        this.optionC = optionC;
    }

    public String getOptionD() {
        return optionD;
    }

    public void setOptionD(String optionD) {
        this.optionD = optionD;
    }

    public String getCorrectAnswer() {
        return correctAnswer;
    }

    public void setCorrectAnswer(String correctAnswer) {
        this.correctAnswer = correctAnswer;
    }
}
